/**
 * @功能: 串口用来调试舵机
 * @参数: 
 * @返回: 
 * @解释: 用轮询的方式等待数据接收，收到数据后，调用pca_servo_20来控制舵机
 */
void Servo_usartReceiveData(void)
{
   HAL_UART_Receive(&huart1, (uint8_t*)rx_buffer, usart_bufferSize, 1000); // 等待接收数据
   if (sscanf((char*)rx_buffer, "%hhu %hhu", &num, &end_angle) == 2) {
        // 调用 PCA_Servo_20 函数控制舵机
        PCA_Servo_20(num, end_angle);
    } else {
        // 解析失败，发送错误信息
        char error_msg[] = "Invalid data format\n";
        HAL_UART_Transmit(&huart1, (uint8_t*)error_msg, sizeof(error_msg) - 1, 1000);
    }
}

